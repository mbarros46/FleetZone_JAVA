CREATE TABLE filial (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    endereco VARCHAR(200),
    cidade VARCHAR(100),
    estado VARCHAR(2),
    cep VARCHAR(10),
    telefone VARCHAR(20),
    email VARCHAR(100)
);

-- Adicionar coluna filial_id na tabela patio
ALTER TABLE patio ADD COLUMN filial_id BIGINT;
ALTER TABLE patio ADD CONSTRAINT fk_patio_filial FOREIGN KEY (filial_id) REFERENCES filial(id);

